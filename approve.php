<?php
include 'config.php';

if ($_SERVER["REQUEST_METHOD"] == "GET" && isset($_GET['id'])) {
    $id = $_GET['id'];

    // Update status pengadaan menjadi 'Disetujui' dan catat tanggal persetujuan
    $tanggalPersetujuan = date("Y-m-d");
    $sqlApprove = "UPDATE tabel_pengadaan SET status = 'Disetujui', tanggal_persetujuan = '$tanggalPersetujuan' WHERE id = $id";

    if ($koneksi->query($sqlApprove) === TRUE) {
       header("Location: dashboard_admin.php");
        exit();
    } else {
        echo "Gagal menyetujui pengadaan.";
    }
} else {
    header("Location: dashboard_admin.php");
    exit();
}
